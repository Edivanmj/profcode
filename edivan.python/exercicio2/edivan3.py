# Solicita a distância entre as cidades em quilômetros
distancia = float(input("Digite a distância entre as cidades (em quilômetros): "))

# Solicita o tempo de viagem em horas
tempo_horas = float(input("Digite o tempo de viagem (em horas): "))

# Calcula a velocidade média
velocidade_media = distancia / tempo_horas

# Exibe a velocidade média
print("A velocidade média do carro é:", velocidade_media, "km/h")
